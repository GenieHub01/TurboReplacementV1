            <p class="all-para thanks">STICKING VANES</p>

            <div class="row">
                <div class="col-md-8">
                    <p class="body-para">WVNT Turbo Vanes have replaced the original and older type of wastegate system on most diesel turbochargers. The reason being to improve fuel economy, to reduce emissions and to make the turbocharger more responsive even at low speeds.</p>

                    <p class="body-para">Turbo Vanes are a row of vanes fitted inside the the turbocharger which will match the boost required from the engine at low engine RPM. The VNT Turbo Vanes will close thereby giving a rapid boost and response time.</p>

                    <p class="body-para">They also lower the LAG prone to the wastegate type turbochargers. When the engine is running at higher revs the VNT turbo vanes open to maximize exhaust flow, to avoid over-speeding or over-boosting and damaging the turbochargers or even the engine.</p>

